package it.uniroma3.diadia.ambienti;

/**
 * Rappresenta le quattro direzioni cardinali utilizzabili all'interno del labirinto:
 * nord, est, sud e ovest.
 * 
 * Ogni direzione è usata per definire i collegamenti tra stanze adiacenti e
 * per determinare la direzione in cui il giocatore può muoversi.
 * 
 * Questa enumerazione consente di gestire in modo sicuro e tipizzato
 * le direzioni all'interno del gioco.
 * 
 * @author Feded0 (609805) e Civan04 (605634)
 * @see Labirinto
 * @version C
 */

public enum Direzione {
	nord, est, sud, ovest
}
